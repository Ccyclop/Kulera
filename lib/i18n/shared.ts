export function normalizeLocale(value: string | null | undefined): Locale {
  return value === "en" ? "en" : DEFAULT_LOCALE;
}

export function nextLocale(locale: Locale): Locale {
  return locale === "ka" ? "en" : "ka";
}

export function translate(locale: Locale, key: string, replacements: Replacements = {}) {
  const template = locale === "en" ? en[key] ?? categoryNameMap[key] ?? key : key;

  return Object.entries(replacements).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

export function getTranslator(locale: Locale) {
  return (key: string, replacements?: Replacements) => translate(locale, key, replacements);
}

export function formatMinutes(locale: Locale, minutes: number) {
  return translate(locale, "{count} წთ", { count: minutes });
}

export function formatRatingCount(locale: Locale, count: number) {
  if (locale === "en") return count === 1 ? "1 rating" : `${count} ratings`;
  return `${count} შეფასება`;
}

export function formatRecipeCount(locale: Locale, count: number) {
  if (locale === "en") return count === 1 ? "1 recipe" : `${count} recipes`;
  return `${count} რეცეპტი`;
}

export function translateDifficulty(locale: Locale, difficulty: Difficulty) {
  return locale === "en" ? difficultyLabels[difficulty] : difficulty;
}

export function translateStatus(locale: Locale, status: RecipeStatus) {
  if (locale === "en") return statusLabels[status];
  if (status === "published") return "გამოქვეყნებული";
  if (status === "draft") return "მონახაზი";
  return "არქივი";
}

export function translateCookBadge(locale: Locale, badge: string) {
  return locale === "en" ? badgeLabels[badge] ?? badge : badge;
}

export function translateStepTitle(locale: Locale, title: string) {
  const match = title.match(/^ნაბიჯი\s+(\d+)$/);
  if (!match?.[1]) return title;
  return translate(locale, "ნაბიჯი {count}", { count: match[1] });
}

export function translateCategoryName(locale: Locale, name: string) {
  return locale === "en" ? categoryNameMap[name] ?? name : name;
}

export function localizeCategory<T extends Pick<Category, "slug" | "name" | "description">>(locale: Locale, category: T): T {
  if (locale !== "en") return category;
  const copy = categoryCopy[category.slug];
  if (!copy) return category;

  return {
    ...category,
    name: copy.name,
    description: copy.description,
  };
}
